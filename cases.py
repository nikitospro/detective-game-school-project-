from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Suspect:
    suspect_id: str
    name: str
    role: str
    short_backstory: str
    personality_traits: list[str]
    speaking_style: str
    motive: str
    alibi: str
    hidden_secrets: list[str]
    known_facts: list[str]
    relationship_to_victim: str
    stress_level: int
    private_strategy: str
    pressure_points: list[str]


@dataclass(frozen=True)
class Clue:
    clue_id: str
    title: str
    description: str
    found_at: str
    interpretation: str
    related_suspects: list[str]
    misleading: bool
    critical: bool
    keywords: list[str]


@dataclass(frozen=True)
class Contradiction:
    contradiction_id: str
    title: str
    description: str
    suspect_ids: list[str]
    clue_ids: list[str]
    severity: int
    unlock_after_questions: int = 1


@dataclass(frozen=True)
class Solution:
    culprit_id: str
    culprit_name: str
    motive: str
    key_evidence: list[str]
    contradictions: list[str]
    logical_explanation: str
    motive_keywords: list[str]
    evidence_keywords: dict[str, list[str]]


@dataclass(frozen=True)
class CaseFile:
    case_id: str
    title: str
    introduction: str
    location: str
    timeline: list[str]
    victim_name: str
    difficulty_label: str
    summary_hint: str
    suspects: list[Suspect]
    clues: list[Clue]
    contradictions: list[Contradiction]
    solution: Solution

    def suspect_by_id(self, suspect_id: str) -> Suspect:
        return next(suspect for suspect in self.suspects if suspect.suspect_id == suspect_id)

    def clue_by_id(self, clue_id: str) -> Clue:
        return next(clue for clue in self.clues if clue.clue_id == clue_id)


